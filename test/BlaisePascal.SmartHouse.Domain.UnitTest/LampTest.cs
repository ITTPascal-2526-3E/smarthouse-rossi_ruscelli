using System;
using System.Security.Cryptography.X509Certificates;
using BlaisePascal.SmartHouse.Domain.Lamps;
using Xunit;
using BlaisePascal.SmartHouse.Domain.Abstractions;

namespace BlaisePascal.SmartHouse.Domain.UnitTest
{
    public class LampTests
    {
        [Fact]
        public void ConstructorTest()
        {
            var lamp = new Lamp(true,"test",ColorType.Orange,100,LampType.LED);
            Assert.True(lamp.IsOnProperty);
            Assert.Equal("test", lamp.NameProperty);
            Assert.Equal(ColorType.Orange, lamp.ColorProperty);
            Assert.Equal(100, lamp.BrightnessProperty);
            Assert.Equal(LampType.LED, lamp.LampTypeProperty);
        }



        [Fact]
        public void ChangeLampType_WhenOn_UpdatesPowerConsumption()
        {
            // lampada accesa, brightness iniziale 100
            var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 100, LampType.LED);

            // cambio tipo di lampada
            lamp.ChangeLampType(LampType.Halogen);

            // calcolo atteso con brightness = 100
            float expected = AbstractLamp.GetMaxConsumption(LampType.Halogen)
                             * (100 / 100f)
                             * AbstractLamp.GetAlpha(LampType.Halogen);

            Assert.Equal(expected, lamp.PowerConsumption, 3);
        }


        [Fact]
            public void ChangeLampType_WhenOff_ConsumptionRemainsZero()
            {
                var lamp = new Lamp(false, "Lamp1", ColorType.CoolWhite, 100, LampType.LED);

                lamp.ChangeLampType(LampType.Halogen);

                Assert.Equal(0, lamp.PowerConsumption);
            }


            [Fact]
            public void ChangeColor_DoesNotAffectPowerConsumption()
            {
                var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 100, LampType.LED);

                float before = lamp.PowerConsumption;

                lamp.ChangeColor(ColorType.Red);

                Assert.Equal(before, lamp.PowerConsumption, 3);
            }


            [Fact]
            public void ChangeBrightness_NegativeValue_IsIgnored()
            {
                var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 50, LampType.LED);
                float before = lamp.PowerConsumption;

                lamp.ChangeBrightness(-10);

               Assert.Equal(before, lamp.PowerConsumption, 3);
            }

            [Fact]
            public void ChangeBrightness_ValueOver100_IsIgnored()
            {
                var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 50, LampType.LED);
                float before = lamp.PowerConsumption;

                lamp.ChangeBrightness(150);

                Assert.Equal(before, lamp.PowerConsumption, 3);
            }

            [Fact]
            public void ChangeBrightness_Zero_WhenOn_ConsumptionBecomesZero()
            {
                var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 50, LampType.LED);

                lamp.ChangeBrightness(0);

                Assert.Equal(0, lamp.PowerConsumption);
            }

            [Fact]
            public void ChangeBrightness_ValidValue_UpdatesConsumption()
            {
                var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 50, LampType.LED);

                lamp.ChangeBrightness(80);

                float expected =
                    AbstractLamp.GetMaxConsumption(LampType.LED)
                    * 0.8f
                    * AbstractLamp.GetAlpha(LampType.LED);

                Assert.Equal(expected, lamp.PowerConsumption, 3);
            }
       

        [Fact]
        public void TurnOn_shouldBeTurnedOn()
        {
            var lamp = new Lamp(false, "Lamp1", ColorType.CoolWhite, 100, LampType.LED);
            lamp.TurnOn();
            Assert.True(lamp.IsOnProperty);
        }
        [Fact]
        public void TurnOff_shouldBeTurnedOff()
        {
            var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 100, LampType.LED);
            lamp.TurnOff();
            Assert.False(lamp.IsOnProperty);
            
        }
        [Fact]
        public void PowerConsumption_WhenOff_IsZero()
        {
            var lamp = new Lamp(false, "Lamp1", ColorType.CoolWhite, 100, LampType.LED);

            Assert.Equal(0, lamp.PowerConsumption);
        }
      
        [Fact]
        public void PowerConsumption_WhenOnAndFullBrightness_IsCorrect()
        {
            var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 100, LampType.LED);

            Assert.Equal(5f, lamp.PowerConsumption, 3);
        }

        [Fact]
        public void TurnOn_EnablesConsumption()
        {
            var lamp = new Lamp(false, "Lamp1", ColorType.CoolWhite, 50, LampType.LED);

            lamp.TurnOn();

            Assert.True(lamp.PowerConsumption > 0);
        }
        [Fact]
        public void ChangeBrightness_WhenOn_UpdatesBrightness()
        {
            var lamp = new Lamp(true,"Lamp1",ColorType.CoolWhite,50,LampType.LED);

            lamp.ChangeBrightness(80);

            Assert.Equal(80, lamp.BrightnessProperty);
        }
        [Fact]
        public void ChangeBrightness_WhenOn_ChangesConsumption()
        {
            var lamp = new Lamp(true, "Lamp1", ColorType.CoolWhite, 50, LampType.LED);

            lamp.ChangeBrightness(80);

            float expected =
                AbstractLamp.GetMaxConsumption(LampType.LED)* 0.8f * AbstractLamp.GetAlpha(LampType.LED);

            Assert.Equal(expected, lamp.PowerConsumption, 3);
        }
        [Fact]
        public void ChangeBrightness_WhenOff_Doesntdonothing()
        {
            var lamp = new Lamp(false, "Lamp1", ColorType.CoolWhite, 50, LampType.LED);

            lamp.ChangeBrightness(80);

          
            Assert.Equal(0,lamp.BrightnessProperty);
        }
    }
}



















































